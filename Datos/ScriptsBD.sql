Create Procedure [dbo].[SP_EQUIPAJECONSULTAR]
(
   @pNROBOLETO Char(10) = Null,
   @pDNI       Char(8)  = Null
)
As
Begin
   Set Nocount On
   If(Len(@pNROBOLETO)>0 Or Len(@pDNI)>0) 
   Begin
   If(@pNROBOLETO='') Set @pNROBOLETO = Null
   If(@pDNI='') Set @pDNI = Null   
   End
   Select 
      a.INT_CODIGOBOLETO,
      a.CHR_NUMEROBOLETO,
      Pasajero    = c.VCH_APEPATPERSONA +' '+ c.VCH_APEMATPERSONA +', '+ c.VCH_NOMBREPERSONA,
      i.DEC_PESO,
      a.VCH_ORIGEN,
      a.VCH_DESTINO,
      FechaSalida = Convert(varchar(20),a.DTE_FECHASALIDA,103),
      HoraSalida  = RIGHT(Convert(datetime,a.DTE_HORASALIDA,108),8),
      j.VCH_NOMBREETIQUETA,
      i.VCH_UBICACION,
      FechaActual = Convert(varchar(20),GETDATE(),103),
      HoraActual  = RIGHT(Convert(datetime, GETDATE(), 108),8),
      i.VCH_ESTADOEQUIPAJE
   From TA_BOLETO (Nolock)       a
   Inner Join TA_PASAJERO        b on a.INT_CODIGOPASAJERO        = b.INT_CODIGOPASAJERO     And b.BLN_ACTIVO = 1
   Inner Join TA_PERSONA         c On b.INT_CODIGOPERSONA      = c.INT_CODIGOPERSONA   And c.BLN_ACTIVO = 1
   Inner Join TA_TIPO_SERVICIO    d On a.INT_CODIGOTIPOSERVICIO = d.INT_CODIGOTIPOSERVICIO
   Inner Join TA_ASIENTOVEHICULO g On a.INT_VEHICULO     = g.INT_VEHICULO  And a.DTE_FECHASALIDA=g.DTE_FECHASALIDA And a.DTE_HORASALIDA=g.DTE_HORASALIDA And a.CHR_ASIENTO=g.CHR_ASIENTO
   Inner Join TA_ESTADOASIENTO   h On g.INT_CODIGOESTASIENTO   = h.INT_CODIGOESTASIENTO
   Inner Join TA_EQUIPAJE        i On a.INT_CODIGOBOLETO       = i.INT_CODIGOBOLETO and i.BLN_ACTIVO = 1
   Inner Join TA_TIPOETIQUETA    j On i.INT_CODIGOETIQUETA     = j.INT_CODIGOETIQUETA
   Where g.INT_CODIGOESTASIENTO <> 3 And i.VCH_ESTADOEQUIPAJE <> 'CANCELADO'
     And a.CHR_NUMEROBOLETO = COALESCE(@pNROBOLETO,a.CHR_NUMEROBOLETO)
     And c.VCH_NUMERODOCUMENTO = COALESCE(@pDNI,c.VCH_NUMERODOCUMENTO)
   Set Nocount Off
End
